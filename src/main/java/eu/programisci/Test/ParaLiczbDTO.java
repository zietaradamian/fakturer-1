package eu.programisci.Test;

public class ParaLiczbDTO {

    private Integer x;
    private Integer y;

    public ParaLiczbDTO() {
    }

    public ParaLiczbDTO(Integer aX, Integer aY) {
        x = aX;
        y = aY;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer aX) {
        x = aX;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer aY) {
        y = aY;
    }
}
